# Порахувати кількість унікальних символів в строці. Якщо їх більше 10 - вивести в консоль True, інакше - False. 
# Строку отримати за допомогою функції input()

input_data = input("Введіть строку даних: ")
print(f"Ви ввели строку: {input_data}")
print(f"Ця строка має унікальних символи: {set(input_data)} ")

print(f"В строці {len((set(input_data)))} унікальних символів ")
print(f"{bool(len((set(input_data)))>10)}")
